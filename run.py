#!/usr/bin/env python3
"""
材料管理システム (matemane) 起動スクリプト

使用方法:
    python run.py
"""

import uvicorn
import sys
import os

# プロジェクトルートをPythonパスに追加
project_root = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, project_root)

if __name__ == "__main__":
    uvicorn.run(
        "src.main:app",
        host="0.0.0.0",
        port=8000,
        reload=True,
        log_level="info"
    )